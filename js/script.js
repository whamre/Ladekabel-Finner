function loadBrands() {
  const brandSelect = document.getElementById('brandSelect');
  for (const brand in carData) {
      const option = document.createElement('option');
      option.value = brand;
      option.text = brand;
      brandSelect.appendChild(option);
  }
}

// Load car models based on selected brand
function loadModels() {
  const modelSelect = document.getElementById('modelSelect');
  const selectedBrand = document.getElementById('brandSelect').value;

  // Hide the information card when changing brands
  hideDetails();

  // Reset model dropdown
  modelSelect.innerHTML = '<option value="">Choose a model</option>';

  if (selectedBrand) {
      const models = carData[selectedBrand];
      for (const model in models) {
          const option = document.createElement('option');
          option.value = model;
          option.text = model;
          modelSelect.appendChild(option);
      }
  }
}

// Display details of the selected car model
function displayDetails() {
  const brandSelect = document.getElementById('brandSelect');
  const modelSelect = document.getElementById('modelSelect');
  const carInfo = document.getElementById('carInfo');
  const selectedBrand = brandSelect.value;
  const selectedModel = modelSelect.value;

  // Hide details if no model is selected
  if (!selectedModel) {
      hideDetails();
      return;
  }

  if (selectedBrand && selectedModel) {
      const details = carData[selectedBrand][selectedModel];

      document.getElementById('carTitle').innerText = `${selectedBrand} ${selectedModel}`;
      document.getElementById('ladekontakt').innerText = details.ladekontakt || 'N/A';
      document.getElementById('batterikapasitet').innerText = details.batterikapasitet || 'N/A';
      document.getElementById('ombordlader').innerText = details.ombordlader || 'N/A';
      document.getElementById('cable45').innerText = details["4m-5m"] ? details["4m-5m"].join(", ") : 'N/A';
      document.getElementById('cable75').innerText = details["7m-7.5m"] ? details["7m-7.5m"].join(", ") : 'N/A';

      carInfo.style.display = 'block';
  }
}

// Hide car information details
function hideDetails() {
  const carInfo = document.getElementById('carInfo');
  carInfo.style.display = 'none';
}

// Initialize the dropdowns when the page loads
window.onload = function () {
  loadBrands();
};